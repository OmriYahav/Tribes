export type Faction = 'cipher' | 'vanguard';
export type Tribe = Faction;
export type ResourceType = 'data' | 'energy' | 'materials';
export type EquipmentItem = 'scanner' | 'relayKey' | 'fieldKit';
export type Coordinates = { latitude: number; longitude: number };
export type User = { id: string; username: string; tribe: Faction; xp: number; level: number };
export type Inventory = Record<ResourceType, number>;
export type Equipment = Record<EquipmentItem, number>;
export type Resource = { id: string; type: ResourceType; coordinates: Coordinates; collected: boolean };
export type BaseLevel = 1 | 2 | 3 | 4 | 5;
export type CampLevel = 0 | 1 | 2 | 3 | 4;
export type Base = { tribe: Faction; coordinates?: Coordinates; level: CampLevel; progress: Inventory; ownerClanId?: string };
export type Camp = Base;
export type Clan = { id: string; name: string; faction: Faction; territory: number; members: number };
export type Player = User & { clan?: Clan; inventory: Inventory; equipment: Equipment; stats: PlayerStats };
export type PlayerStats = { totalXp: number; level: number; resourcesCollected: number; itemsCrafted: number; resourcesContributed: number; playersGreeted: number; campContributionScore: number; missionsCompleted: number; distanceMeters: number };
export type MockPlayer = { id: string; username: string; tribe: Faction; level: number; coordinates: Coordinates; distance: number; status: string };
export type LeaderboardEntry = { id: string; username: string; tribe: Faction; level: number; totalXp: number; contributionScore: number; missions: number; rank?: number; isCurrentUser?: boolean };
export type MissionStatus = 'available' | 'active' | 'completed' | 'claimed' | 'locked';
export type MissionFilter = 'nearby' | 'clan' | 'daily' | 'special';
export type Mission = { id: string; title: string; objective: string; difficulty: 'easy' | 'medium' | 'hard' | 'elite'; distanceMeters: number; reward: string; progress: number; target: number; status: MissionStatus; filter: MissionFilter };
export type Achievement = { id: string; name: string; description: string; unlocked: boolean; progress?: number; target?: number };
export type Badge = Achievement;
export type InventoryItem = { id: EquipmentItem; name: string; description: string; quantity: number; cost: Partial<Inventory>; available: boolean; disabledReason?: string };
export type GameActionResult = { ok: boolean; message: string; xpAwarded?: number; resources?: Partial<Inventory> };
